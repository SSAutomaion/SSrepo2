
public class StarhillU {
	
		public static void main(String[] args) {

			int n = 5;
			for (int a=1; a<=n; a++ )	
			{
			for (int b=a; b<=n; b++ )
			{
				System.out.print("  ");
			}
			for (int b=1; b<a; b++ )
			{
				System.out.print(" *");
			}
			for (int b=1; b<=a; b++)
			{
				System.out.print(" *");
			}
			System.out.println();
			}		
		
		  }
        }
